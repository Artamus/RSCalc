package com.rscalc;

public class OtherBonuses {
    private int meleeStrength;
    private int rangedStrength;
    private int magicStrength;
    private int prayerBonus;

    public OtherBonuses(int meleeStrength,
                        int rangedStrength,
                        int magicStrength,
                        int prayerBonus){
        this.meleeStrength = meleeStrength;
        this.rangedStrength = rangedStrength;
        this.magicStrength = magicStrength;
        this.prayerBonus = prayerBonus;
    }
}
